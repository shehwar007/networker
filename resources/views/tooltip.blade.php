<div id="tool_activity" class=" tippy-popper tippy-tooltip tippy-content text-left">
    Activities on the drop-down list are ordered as follows:</br></br>

    -The activities towards the top half tend to feature direct contact, require more time and effort,</br>
    and have more impact on chemistry and credibility.</br></br></br>

    -The activities towards the bottom half tend to feature remote contact, require less time and effort,</br>
    and have less impact on chemistry but can build credibility.</br></br></br>

    Seminars and webinars tend to be an exception. They take more work and may be stressful, but you have</br>
    more direct contact. This activity also means more opportunity to build chemistry and credibility with</br>
    a higher number of connections.</br></br></br>

    Also, ensure you choose activity:</br></br></br>

    - You can realistically complete for all listed A1 and A2 connections over a 1–3-month period. That is,</br>
    avoid trying to do too much given your other commitments.</br>
    - You and the connection enjoy.</br></br></br>

    - Represent the best ROTI. Note return could mean immediate business or business over the long-term because </br>
    it builds a relationship or raises profile. Therefore, be clear on what return is realistic for an activity.</br>
    For instance, the realistic return for presenting a seminar is raising profile rather than immediate business.</br>
</div>


<div id="tool_help" class=" tippy-popper tippy-tooltip tippy-content text-left">


    Effective networkers are always finding ways to help their connections. Therefore ensure your</br>
    activity includes help for the connection. Ways to help include the following:</br></br>

    Connect them to somebody – Can you connect them to a potential client or provider who might resolve a need?</br>
    Maybe you can connect them to others who work in their field or share the same interests?</br></br>

    Give them insights – Can you give them insights that are relevant to their business, role or</br>
    personal interests?</br></br></br>

    Give them a sounding board – Can you spend time with them so they can reflect on their business</br>
    challenges or goals and therefore crystallise their thinking?</br></br></br>

    Give them something – Is it appropriate to gift them something for Xmas or their birthday etc?</br>
    Personal gifts might relate to their interests e.g., sport, food, or gadgets. Maybe gift something</br>
    relevant to their business role or provide an incentive or reward?</br></br></br>

    Give them an opportunity to contribute – Maybe you are working on a project, and they would appreciate</br>
    being involved? This might include surveys.</br></br></br>

    Give them updates - Maybe they would appreciate being kept in the loop on certain developments.</br></br>

    Support them – Can you provide moral or practical support for a project they are working on?</br></br>

    Recognise their contributions or achievements – Acnowledge or reward their contributions where appropriate.</br>
    LinkedIn is a good source for opportunities to recognise achievements such as an award, promotion, or new</br>
    role. Maybe you can provide a testimonial?


</div>