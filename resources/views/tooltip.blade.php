<div id="tool_name" class=" tippy-popper tippy-tooltip tippy-content text-left">

    A connection is:</br></br>
    An existing client for whom you are not currently providing a service; or
    Someone who has or might refer you to a potential client.</br></br>

    Also, exclude connections for specific opportunities you are currently pursuing.</br></br>

    The connection may be an individual or a group of individuals.

</div>

<div id="tool_type" class=" tippy-popper tippy-tooltip tippy-content text-left">

    A connection type is as follows:</br>
    Type A1. Higher ROTI score/Higher relationship score and therefore requires less attention</br></br>
    Type A2. Higher ROTI score/Lower relationship score and therefore requires more attention. </br>
    </br>
    Definitions:</br>
    </br>
    ROTI = return on time invested. That is, the short- or long-term value the connection could</br>
    provide verse the time and effort to develop the connection. Value returned could be business</br>
    or introductions to business. Also, consider the likelihood of the connection delivering that return.</br>
    </br>
    ROTI score = Determined by benchmarking the connection against your most valuable client who represents</br>
    the best return on time invested. That client is rated 10/10. Give your connection a ROTI score out of</br>
    10 compared to your benchmark client. A higher ROTI score for a connection is 7-10/10</br>
    </br>
    Relationship score = Determined by benchmarking the connection against your biggest fan.</br>
    That is, the person with whom you have the strongest relationship because you have a lot of chemistry,</br>
    and they find you highly credible. That person is rated 10/10. Give your connection a relationship score</br>
    out of 10 compared to your benchmark connection. A higher relationship score for a connection is 7-10/10.</br>
    Attention required = The time, effort and frequency of contact needed to maintain and build the relationship.</br>
    </br>
    Note:</br>
    </br>
    - If you have just met a connection then clasiify them as a A2 even if the initail contact was very positive.</br>
    You need to meet with them 2 or 3 more times before classifying them as an A1 assuming the chemistry is still</br>
    strong and they still find you credible.</br>
    </br>
    - Your frequency of contact with a strong connection can affect the score. That is, a strong connection may need</br>
    attention if the duration since last contact is over six months.</br>

</div>

